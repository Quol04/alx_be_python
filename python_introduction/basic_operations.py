# To practice basic arithmetic operations in Python by performing predefined calculations.

number1 = 10
number2 = 5

# Addition
sum_result = number1 + number2
print("Addition of", number1, "and", number2, "is", sum_result)

# Subtraction
subtraction_result = number1 - number2
print("Subtraction of", number2, "from", number1, "is", subtraction_result)

# Multiplication
multiplication_result = number1 * number2
print("Multiplication of", number1, "and", number2, "is", multiplication_result)

